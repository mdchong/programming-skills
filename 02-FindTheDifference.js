/*
    You are given two strings 's' and 't'

    String t is generated by random shuffling string 's' and then add one more letter at a random position

    Return the letter that was add to to t

    Example 1:
        Input: s = "abcd", t = "abcde"
        Output: "e"
        Explanation: 'e' is the letter that was added

    Example 2:
        Input: s = "", t = "y"
        Output: "y"
*/

var findTheDifference = function (s, t) {
    // Loop through the given strings and find the sum of each s and t
        // Find the ASCII Value!
    // Find the difference between the two values
    // Translate it back to a letter and we will find the difference
    let sumS = 0;
    let sumT = 0;

    for (let char of s) {
        sumS += char.charCodeAt();
    }

    for (let char of t) {
        sumT += char.charCodeAt();
    }

    return String.fromCharCode(sumT - sumS);
}

s = "abcd";
t = "abcde";

console.log(findTheDifference(s, t))